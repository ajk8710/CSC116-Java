/**
 * This program computes pattern of increment by increasing odds
 *
 * @author Alex Jihong Kim
 */
public class SeriesOfNumbers {
	/**
	 * Starts the program.
	 *
	 * @param args
	 *	          command line arguments
	 */  
	public static void main(String[] args) {
		for (int i = 1; i <= 10; i++) {
		System.out.print(i*i + " ");
        }
		System.out.println(" ");
	}
}
